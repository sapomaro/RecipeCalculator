export function normalizeRecipeNumbers(text, lang = 'ru') {
  if (lang !== 'ru') {
    return '';
  }

  // TODO: здесь нужен рефакторинг
	return text //.trim() //.toLowerCase()
    .replace(/Ё/g, 'Е')
    .replace(/ё/g, 'е')
    .replace(/(\d)\-(й|х|ух|ех|ти)(\s|$)/gi, "$1$3")
    .replace(/(^|\s|\.)(1\/2)(\s|$)/g, "$1"+"0.5"+"$3").replace(/½/g, ' 0.5')
    .replace(/(^|\s|\.)(1\/4)(\s|$)/g, "$1"+"0.25"+"$3").replace(/¼/g, ' 0.25')
    .replace(/(^|\s|\.)(3\/4)(\s|$)/g, "$1"+"0.75"+"$3").replace(/¾/g, ' 0.75')
    .replace(/(^|\s|\.)(1\/7)(\s|$)/g, "$1"+"0.14"+"$3").replace(/⅐/g, ' 0.14')
    .replace(/(^|\s|\.)(1\/9)(\s|$)/g, "$1"+"0.11"+"$3").replace(/⅑/g, ' 0.11')
    .replace(/(^|\s|\.)(1\/10)(\s|$)/g, "$1"+"0.1"+"$3").replace(/⅒/g, ' 0.1')
    .replace(/(^|\s|\.)(1\/3|одна треть)(\s|$)/g, "$1"+"0.33"+"$3").replace(/⅓/g, ' 0.33')
    .replace(/(^|\s|\.)(2\/3|две трети)(\s|$)/g, "$1"+"0.66"+"$3").replace(/⅔/g, ' 0.66')
    .replace(/(^|\s|\.)(1\/5)(\s|$)/g, "$1"+"0.2"+"$3").replace(/⅕/g, ' 0.2')
    .replace(/(^|\s|\.)(2\/5)(\s|$)/g, "$1"+"0.4"+"$3").replace(/⅖/g, ' 0.4')
    .replace(/(^|\s|\.)(3\/5)(\s|$)/g, "$1"+"0.6"+"$3").replace(/⅗/g, ' 0.6')
    .replace(/(^|\s|\.)(4\/5)(\s|$)/g, "$1"+"0.8"+"$3").replace(/⅘/g, ' 0.8')
    .replace(/(^|\s|\.)(1\/6)(\s|$)/g, "$1"+"0.17"+"$3").replace(/⅙/g, ' 0.17')
    .replace(/(^|\s|\.)(5\/6)(\s|$)/g, "$1"+"0.83"+"$3").replace(/⅚/g, ' 0.83')
    .replace(/(^|\s|\.)(1\/8)(\s|$)/g, "$1"+"0.125"+"$3").replace(/⅛/g, ' 0.125')
    .replace(/(^|\s|\.)(3\/8)(\s|$)/g, "$1"+"0.375"+"$3").replace(/⅜/g, ' 0.375')
    .replace(/(^|\s|\.)(5\/8)(\s|$)/g, "$1"+"0.625"+"$3").replace(/⅝/g, ' 0.625')
    .replace(/(^|\s|\.)(7\/8)(\s|$)/g, "$1"+"0.875"+"$3").replace(/⅞/g, ' 0.875')
    .replace(/(^|\s|\.)(один|одного|одна|одной|одну|одно)(-|\s|$)/gi, "$1"+"1"+"$3")
    .replace(/(^|\s|\.|-)(два|две|двух)(-|\s|$)/gi, "$1"+"2"+"$3")
    .replace(/(^|\s|\.|-)(три|трех)(-|\s|$)/gi, "$1"+"3"+"$3")
    .replace(/(^|\s|\.|-)(четыре|четырех)(-|\s|$)/gi, "$1"+"4"+"$3")
    .replace(/(^|\s|\.|-)(пять|пяти|пятерых)(-|\s|$)/gi, "$1"+"5"+"$3")
    .replace(/(^|\s|\.|-)(шесть|шести|шестерых)(-|\s|$)/gi, "$1"+"6"+"$3")
    .replace(/(^|\s|\.|-)(семь|семи|семерых)(-|\s|$)/gi, "$1"+"7"+"$3")
    .replace(/(^|\s|\.|-)(восемь|восьми|восьмерых)(-|\s|$)/gi, "$1"+"8"+"$3")
    .replace(/(^|\s|\.|-)(девять|девяти)(-|\s|$)/gi, "$1"+"9"+"$3")
    .replace(/(^|\s|\.|-)(десять|десяти)(-|\s|$)/gi, "$1"+"10"+"$3")
    .replace(/1-2/g, "1.5")
    .replace(/1-3/g, "2")
    .replace(/2-3/g, "2.5")
    .replace(/2-4/g, "3")
    .replace(/3-4/g, "3.5")
    .replace(/4-5/g, "4.5")
    .replace(/5-6/g, "5.5")
    .replace(/6-7/g, "6.5")
    .replace(/7-8/g, "7.5")
    .replace(/8-9/g, "8.5")
    .replace(/9-10/g, "9.5")
    .replace(/(\d)\,(\d)/g, "$1"+"."+"$2")
    .replace(/(\d+)\s+0\.(\d)/g, "$1"+"."+"$2")
    .replace(/ /g, "")
    .replace(/\(\s(\d)/g, "($1");
}
